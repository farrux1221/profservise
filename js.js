let currentLanguage = 'ru';

// Tilni o'zgartirish funksiyasi
function changeLanguage(lang) {
    currentLanguage = lang;
    
    // Barcha tarjima qilinadigan elementlarni topish
    const elements = document.querySelectorAll('[data-key]');
    
    // Har bir elementga tarjima qo'llash
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Til tugmalarini yangilash
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
    
    // Til localStorage'ga saqlash
    localStorage.setItem('preferredLanguage', lang);
    
    // HTML lang atributini yangilash
    document.documentElement.lang = lang;
}

// Sahifa yuklanganda
document.addEventListener('DOMContentLoaded', function() {
    // Oldin tanlangan tilni tekshirish
    const savedLanguage = localStorage.getItem('preferredLanguage') || 'ru';
    changeLanguage(savedLanguage);
    
    // Til tugmalariga hodisa qo'shish
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
        });
    });
    
    // AOS animatsiyalari
    if (typeof AOS !== 'undefined') {
        AOS.init({
            duration: 1000,
            easing: 'ease-in-out',
            once: true,
            mirror: false,
        });
    }
});
// Mobil menyu funksiyasi
document.addEventListener('DOMContentLoaded', function() {
    const mobileMenu = document.getElementById('mobile-menu');
    const navLinks = document.getElementById('nav-links');
    
    if (mobileMenu && navLinks) {
        mobileMenu.addEventListener('click', function() {
            navLinks.classList.toggle('active');
            mobileMenu.classList.toggle('active');
        });
        
        // Menyu elementlariga bosilganda menyu yopilishi
        const menuItems = document.querySelectorAll('.nav-links a');
        menuItems.forEach(item => {
            item.addEventListener('click', function() {
                navLinks.classList.remove('active');
                mobileMenu.classList.remove('active');
            });
        });
    }
    
    // Silliq skroll
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
});